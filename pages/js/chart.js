var ctx = document.getElementById("chart-bars").getContext("2d");

// Fetch data from the PHP endpoint using the fetch API
fetch('http://localhost:3010/api/scraped-data')
    .then(response => response.json())
    .then(data => {
        const currentTime = new Date().getHours().toString().padStart(2, '0');
        const currentHour = `${currentTime}:00`;
        // Extract the times and prices from the JSON data
        const times = data.current_day.map(item => Object.keys(item)[0]);
        const prices = data.current_day.map(item => item[Object.keys(item)[0]]);

        // Find the data entry for the current hour
        const currentDataEntry = data.current_day.find(item => Object.keys(item)[0] === currentHour);

        if (currentDataEntry) {
            // Extract the current price
            const currentPrice = currentDataEntry[currentHour];
            document.getElementById('current_price').textContent = currentPrice + ' â‚¬ ';
            console.log(`Current price for ${currentHour} is ${currentPrice}`);
        } else {
            console.log(`Data not available for the current hour (${currentHour}:00).`);
        }

        // Update the data attribute of your chart with the fetched data
        var chartData = {
            labels: times,
            datasets: [{
                label: "Price",
                tension: 0,
                pointRadius: 5,
                pointBackgroundColor: "rgba(255, 255, 255, .8)",
                pointBorderColor: "transparent",
                borderColor: "rgba(255, 255, 255, .8)",
                borderWidth: 4,
                backgroundColor: "transparent",
                fill: true,
                data: prices,
                maxBarThickness: 6
            }]
        };

        new Chart(ctx, {
            type: "line",
            data: chartData,
            options: {
                responsive: true,
                maintainAspectRatio: false,
                plugins: {
                    legend: {
                        display: false,
                    }
                },
                interaction: {
                    intersect: false,
                    mode: 'index',
                },
                scales: {
                    y: {
                        grid: {
                            drawBorder: false,
                            display: true,
                            drawOnChartArea: true,
                            drawTicks: false,
                            borderDash: [5, 5],
                            color: 'rgba(255, 255, 255, .2)'
                        },
                        ticks: {
                            display: true,
                            color: '#f8f9fa',
                            padding: 10,
                            font: {
                                size: 14,
                                weight: 300,
                                family: "Roboto",
                                style: 'normal',
                                lineHeight: 2
                            },
                        }
                    },
                    x: {
                        grid: {
                            drawBorder: false,
                            display: false,
                            drawOnChartArea: false,
                            drawTicks: false,
                            borderDash: [5, 5]
                        },
                        ticks: {
                            display: true,
                            color: '#f8f9fa',
                            padding: 10,
                            font: {
                                size: 14,
                                weight: 300,
                                family: "Roboto",
                                style: 'normal',
                                lineHeight: 2
                            },
                        }
                    },
                },
            },
        });
    })


